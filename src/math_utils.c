#include "fdf.h"

void	iso(int *x, int *y, int z)
{
	int	x_tmp;
	int	y_tmp;

	x_tmp = *x;
	y_tmp = *y;
	*x = (x_tmp - y_tmp) * atan(cos(M_PI_4));
}
